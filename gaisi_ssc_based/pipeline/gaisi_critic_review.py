def build_critic_prompt(sug_code: str, title: str, description: str,
                        tasks: list, classifications: list) -> str:
    """Build user prompt for critic review of a single occupation."""

    # Occupation details block
    occupation_block = f"""occupation_code: {sug_code}
occupation_title: {title}
occupation_description: {description}"""

    # Task list (original tasks)
    task_lines = "\n".join(f"{t['task_id']}\t{t['task_statement']}" for t in tasks)

    # Classification output table
    class_header = "taskid, e0, e1, e2, e3, justification"
    class_lines = "\n".join(
        f"{c['taskid']}, {c['e0']}, {c['e1']}, {c['e2']}, {c['e3']}, \"{c['justification']}\""
        for c in classifications
    )

    return (
        f"<occupation_details>\n{occupation_block}\n</occupation_details>\n\n"
        f"<task_list>\n{task_lines}\n</task_list>\n\n"
        f"<classification_output>\n{class_header}\n{class_lines}\n</classification_output>"
    )


def generate_critic_batch_requests(desc_df: pd.DataFrame, tasks_df: pd.DataFrame,
                                    class_df: pd.DataFrame, output_path: Path) -> int:
    """Generate JSONL file with batch requests for critic review."""

    request_count = 0
    skipped = []

    with open(output_path, 'w') as f:
        for sug_code in class_df['sugcode'].unique():
            # Get occupation details
            desc_row = desc_df[desc_df['sug_code'] == sug_code]
            if desc_row.empty:
                skipped.append((sug_code, 'no description'))
                continue
            desc_row = desc_row.iloc[0]

            # Get original tasks
            occ_tasks = tasks_df[tasks_df['sug_code'] == sug_code]
            if occ_tasks.empty:
                skipped.append((sug_code, 'no tasks'))
                continue
            tasks = [
                {'task_id': r['task_id'], 'task_statement': r['task_statement']}
                for _, r in occ_tasks.iterrows()
            ]

            # Get classifications for this occupation
            occ_class = class_df[class_df['sugcode'] == sug_code]
            classifications = [
                {
                    'taskid': r['taskid'],
                    'e0': r['e0'],
                    'e1': r['e1'],
                    'e2': r['e2'],
                    'e3': r['e3'],
                    'justification': r['justification']
                }
                for _, r in occ_class.iterrows()
            ]

            # Build prompt
            user_prompt = build_critic_prompt(
                sug_code,
                desc_row['title'],
                desc_row['description'],
                tasks,
                classifications
            )

            request_obj = {
                "key": sug_code,
                "request": {
                    "contents": [{"role": "user", "parts": [{"text": user_prompt}]}],
                    "systemInstruction": {"parts": [{"text": CRITIC_SYSTEM_PROMPT}]},
                    "generationConfig": {
                        "maxOutputTokens": 8192,
                        "thinking_config": {
                            "thinking_budget": THINKING_BUDGET
                        }
                    }
                }
            }

            f.write(json.dumps(request_obj) + "\n")
            request_count += 1

    if skipped:
        logger.warning(f"Skipped {len(skipped)} occupations:")
        for code, reason in skipped[:10]:
            logger.warning(f"  {code}: {reason}")

    return request_count


