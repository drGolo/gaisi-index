def extract_response_text(result: dict) -> str:
    """Extract text from batch result, skipping thinking tokens."""
    if not result.get('response'):
        return ""

    try:
        parts = result['response']['candidates'][0]['content']['parts']
        for part in parts:
            if 'text' in part:
                return part['text']
    except (KeyError, IndexError):
        pass
    return ""


def parse_critic_row(line: str) -> dict:
    """Parse a single CSV row from critic output."""
    line = line.strip()
    if not line or line.startswith('#') or line.startswith('soccode'):
        return None

    # Remove markdown artifacts
    if line.startswith('```') or line.startswith('---'):
        return None

    # Pattern: sugcode, taskid, confidence, "critique"
    # Confidence is 0 or 1
    match = re.match(
        r'([^,]+),\s*([^,]+),\s*([01](?:\.0)?),\s*["\']?(.+?)["\']?\s*$',
        line
    )

    if match:
        return {
            'sugcode': match.group(1).strip(),
            'taskid': match.group(2).strip(),
            'critic_confidence': int(float(match.group(3).strip())),
            'critic_critique': match.group(4).strip()
        }

    # Fallback: simple split
    parts = line.split(',')
    if len(parts) >= 4:
        try:
            confidence = int(float(parts[2].strip()))
        except ValueError:
            confidence = -1  # Parse error marker

        return {
            'sugcode': parts[0].strip(),
            'taskid': parts[1].strip(),
            'critic_confidence': confidence,
            'critic_critique': ','.join(parts[3:]).strip().strip('"\'')
        }

    return None


def parse_critic_results(results_file: Path, output_file: Path):
    """Parse critic batch results to CSV."""

    all_rows = []
    errors = []

    with open(results_file, 'r') as f:
        for line_num, line in enumerate(f, 1):
            if not line.strip():
                continue

            result = json.loads(line)
            sug_code = result.get('key', f'unknown_{line_num}')

            if result.get('error'):
                errors.append({'sug_code': sug_code, 'error': result['error']})
                continue

            text = extract_response_text(result)
            if not text:
                errors.append({'sug_code': sug_code, 'error': 'Empty response'})
                continue

            occupation_rows = 0
            for output_line in text.splitlines():
                parsed = parse_critic_row(output_line)
                if parsed:
                    all_rows.append(parsed)
                    occupation_rows += 1

            if occupation_rows == 0:
                errors.append({'sug_code': sug_code, 'error': 'No parseable rows'})

    # Write CSV
    with open(output_file, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=['sugcode', 'taskid', 'critic_confidence', 'critic_critique'])
        writer.writeheader()
        writer.writerows(all_rows)

    return len(all_rows), errors


