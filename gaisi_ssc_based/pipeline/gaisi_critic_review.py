def merge_classifications_and_reviews(class_file: Path, critic_file: Path,
                                       output_file: Path) -> pd.DataFrame:
    """Merge original classifications with critic reviews."""

    # Load both files
    class_df = pd.read_csv(class_file, dtype=str)
    critic_df = pd.read_csv(critic_file, dtype=str)

    # Standardize columns
    class_df.columns = class_df.columns.str.strip().str.lower()
    critic_df.columns = critic_df.columns.str.strip().str.lower()

    # Clean keys
    class_df['sugcode'] = class_df['sugcode'].str.strip()
    class_df['taskid'] = class_df['taskid'].str.strip()
    critic_df['sugcode'] = critic_df['sugcode'].str.strip()
    critic_df['taskid'] = critic_df['taskid'].str.strip()

    # Merge
    merged = class_df.merge(
        critic_df[['sugcode', 'taskid', 'critic_confidence', 'critic_critique']],
        on=['sugcode', 'taskid'],
        how='left'
    )

    # Report merge stats
    n_matched = merged['critic_confidence'].notna().sum()
    n_missing = merged['critic_confidence'].isna().sum()

    print(f"Classifications: {len(class_df)}")
    print(f"Critic reviews: {len(critic_df)}")
    print(f"Matched: {n_matched}")
    print(f"Missing reviews: {n_missing}")

    # Fill missing
    merged['critic_confidence'] = merged['critic_confidence'].fillna('-1')
    merged['critic_critique'] = merged['critic_critique'].fillna('Review missing')

    # Save
    merged.to_csv(output_file, index=False)

    return merged


