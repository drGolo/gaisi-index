def extract_response_text(result: dict) -> str:
    """Extract text from batch result, skipping thinking tokens."""
    if not result.get('response'):
        return ""

    try:
        parts = result['response']['candidates'][0]['content']['parts']
        # Find the text part (skip thinking parts if present)
        for part in parts:
            if 'text' in part:
                return part['text']
    except (KeyError, IndexError):
        pass
    return ""


def parse_classification_row(line: str) -> dict:
    """Parse a single CSV row from model output."""
    # Clean up the line
    line = line.strip()
    if not line or line.startswith('#') or line.startswith('sug_code'):
        return None

    # Remove markdown code block markers if present
    if line.startswith('```') or line.startswith('---'):
        return None

    # Split by comma, handling quoted justification
    # Pattern: sug_code, task_id, e0, e1, e2, e3, "justification"
    match = re.match(
        r'([^,]+),\s*([^,]+),\s*([0-9.]+),\s*([0-9.]+),\s*([0-9.]+),\s*([0-9.]+),\s*["\']?(.+?)["\']?\s*$',
        line
    )

    if match:
        return {
            'sugcode': match.group(1).strip(),
            'taskid': match.group(2).strip(),
            'e0': match.group(3).strip(),
            'e1': match.group(4).strip(),
            'e2': match.group(5).strip(),
            'e3': match.group(6).strip(),
            'justification': match.group(7).strip()
        }

    # Fallback: simple split (may fail on commas in justification)
    parts = line.split(',')
    if len(parts) >= 7:
        return {
            'sugcode': parts[0].strip(),
            'taskid': parts[1].strip(),
            'e0': parts[2].strip(),
            'e1': parts[3].strip(),
            'e2': parts[4].strip(),
            'e3': parts[5].strip(),
            'justification': ','.join(parts[6:]).strip().strip('"\'')
        }

    return None


def parse_results_to_csv(results_file: Path, output_file: Path):
    """Parse batch results JSONL to flat CSV."""

    all_rows = []
    errors = []

    with open(results_file, 'r') as f:
        for line_num, line in enumerate(f, 1):
            if not line.strip():
                continue

            result = json.loads(line)
            sug_code = result.get('key', f'unknown_{line_num}')

            # Check for errors
            if result.get('error'):
                errors.append({'sug_code': sug_code, 'error': result['error']})
                continue

            # Extract and parse response text
            text = extract_response_text(result)
            if not text:
                errors.append({'sug_code': sug_code, 'error': 'Empty response'})
                continue

            # Parse each line of the CSV output
            occupation_rows = 0
            for output_line in text.splitlines():
                parsed = parse_classification_row(output_line)
                if parsed:
                    all_rows.append(parsed)
                    occupation_rows += 1

            if occupation_rows == 0:
                errors.append({'sug_code': sug_code, 'error': 'No parseable rows'})

    # Write CSV
    with open(output_file, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=['sugcode', 'taskid', 'e0', 'e1', 'e2', 'e3', 'justification'])
        writer.writeheader()
        writer.writerows(all_rows)

    return len(all_rows), errors


