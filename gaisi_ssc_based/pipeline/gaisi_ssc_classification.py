def load_data():
    """Load and merge occupation descriptions with tasks."""

    # Load descriptions
    logger.info(f"Loading: {DESCRIPTIONS_FILE.name}")
    desc_df = pd.read_csv(DESCRIPTIONS_FILE, dtype=str)
    desc_df = desc_df.rename(columns={
        DESC_COLS['code']: 'sug_code',
        DESC_COLS['title']: 'title',
        DESC_COLS['description']: 'description'
    })
    desc_df['sug_code'] = desc_df['sug_code'].str.strip()

    # Load tasks
    logger.info(f"Loading: {TASKS_FILE.name}")
    tasks_df = pd.read_csv(TASKS_FILE, dtype=str)
    tasks_df = tasks_df.rename(columns={
        TASK_COLS['code']: 'sug_code',
        TASK_COLS['id']: 'task_id',
        TASK_COLS['statement']: 'task_statement'
    })
    tasks_df['sug_code'] = tasks_df['sug_code'].str.strip()

    # Merge
    merged = desc_df.merge(tasks_df, on='sug_code', how='inner')

    logger.info(f"Merged: {len(merged)} task-occupation pairs, {merged['sug_code'].nunique()} occupations")

    return merged

