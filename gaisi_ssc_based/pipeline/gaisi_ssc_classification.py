def build_prompt(sug_code: str, title: str, description: str, tasks: list) -> str:
    """Build user prompt for a single occupation."""
    occupation_block = f"""occupation_code: {sug_code}
occupation_title: {title}
occupation_description: {description}"""

    task_lines = "\n".join(f"{t['task_id']}\t{t['task_statement']}" for t in tasks)

    return (
        f"<occupation_details>\n{occupation_block}\n</occupation_details>\n\n"
        f"<task_list>\n{task_lines}\n</task_list>"
    )


def generate_batch_requests(df: pd.DataFrame, output_path: Path) -> int:
    """Generate JSONL file with batch requests."""

    request_count = 0

    with open(output_path, 'w') as f:
        for sug_code, group in df.groupby('sug_code'):
            row = group.iloc[0]
            tasks = [
                {'task_id': r['task_id'], 'task_statement': r['task_statement']}
                for _, r in group.iterrows()
            ]

            user_prompt = build_prompt(sug_code, row['title'], row['description'], tasks)

            request_obj = {
                "key": sug_code,
                "request": {
                    "contents": [{"role": "user", "parts": [{"text": user_prompt}]}],
                    "systemInstruction": {"parts": [{"text": SYSTEM_PROMPT}]},
                    "generationConfig": {
                        "maxOutputTokens": 8192,
                        "thinking_config": {
                            "thinking_budget": THINKING_BUDGET
                        }
                    }
                }
            }

            f.write(json.dumps(request_obj) + "\n")
            request_count += 1

    return request_count


