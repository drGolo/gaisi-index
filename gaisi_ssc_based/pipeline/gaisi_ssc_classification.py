def find_and_clean_encoding_issues(filepath: Path, preview_only: bool = True):
    """
    Find non-UTF-8 bytes in a file and optionally clean them.

    Args:
        filepath: Path to the file
        preview_only: If True, just report issues. If False, create cleaned file.
    """

    print(f"\nChecking: {filepath.name}")

    # Read as binary
    with open(filepath, 'rb') as f:
        raw_bytes = f.read()

    issues = []

    # Try to decode, tracking problem positions
    i = 0
    while i < len(raw_bytes):
        # Try decoding from this position
        try:
            # Check if this byte starts a valid UTF-8 sequence
            if raw_bytes[i] < 128:
                # ASCII - always valid
                i += 1
            elif raw_bytes[i] < 192:
                # Continuation byte without start - invalid
                issues.append((i, raw_bytes[i:i+1]))
                i += 1
            elif raw_bytes[i] < 224:
                # 2-byte sequence
                raw_bytes[i:i+2].decode('utf-8')
                i += 2
            elif raw_bytes[i] < 240:
                # 3-byte sequence
                raw_bytes[i:i+3].decode('utf-8')
                i += 3
            elif raw_bytes[i] < 248:
                # 4-byte sequence
                raw_bytes[i:i+4].decode('utf-8')
                i += 4
            else:
                # Invalid start byte
                issues.append((i, raw_bytes[i:i+1]))
                i += 1
        except (UnicodeDecodeError, IndexError):
            issues.append((i, raw_bytes[i:i+1]))
            i += 1

    if not issues:
        print(f"  ✅ No encoding issues found")
        return

    print(f"  ⚠️  Found {len(issues)} problematic byte(s)")

    # Show context for each issue
    for pos, bad_byte in issues[:20]:  # Show first 20
        # Get surrounding context
        start = max(0, pos - 30)
        end = min(len(raw_bytes), pos + 30)

        # Decode context with replacement
        before = raw_bytes[start:pos].decode('utf-8', errors='replace')
        after = raw_bytes[pos+1:end].decode('utf-8', errors='replace')

        print(f"  Position {pos}: byte 0x{bad_byte.hex()} | ...{before[-20:]}[?]{after[:20]}...")

    if len(issues) > 20:
        print(f"  ... and {len(issues) - 20} more")

    if preview_only:
        print(f"\n  Run with preview_only=False to create cleaned file")
        return

    # Create cleaned version
    cleaned = raw_bytes.decode('utf-8', errors='replace')  # Replace bad bytes with �
    # Or remove entirely:
    # cleaned = raw_bytes.decode('utf-8', errors='ignore')

    clean_path = filepath.with_suffix('.clean.csv')
    with open(clean_path, 'w', encoding='utf-8') as f:
        f.write(cleaned)

    print(f"  ✅ Cleaned file saved: {clean_path.name}")
    print(f"     (Bad bytes replaced with '�' - review and fix manually if needed)")

    return clean_path


